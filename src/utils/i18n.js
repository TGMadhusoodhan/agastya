// Merge with English fallback for any missing keys
function withFallback(lang) {
  if (lang === 'English' || !T[lang]) return T.English
  const en = T.English
  const tl = T[lang]
  const merged = {}
  for (const section of Object.keys(en)) {
    if (!tl[section]) {
      merged[section] = en[section]
    } else {
      merged[section] = { ...en[section], ...tl[section] }
      // Merge nested tip objects
      if (en[section].tips && tl[section].tips) {
        merged[section].tips = { ...en[section].tips, ...tl[section].tips }
      }
    }
  }
  return merged
}

// Cache merged translations
const cache = {}
export function getT(language) {
  if (!cache[language]) cache[language] = withFallback(language)
  return cache[language]
}
